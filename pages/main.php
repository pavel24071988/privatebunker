            <div class="scheme-content-item electricity"  style="display: none;">
Дизель электростанция: 
Обеспечивает автономное питание электроэнергией защитного сооружения. Дополнительно 
возможна установка автономных аккумуляторов.
            </div>
            <div class="scheme-content-item communication"  style="display: none;">
Связь: 
Усилители сотовой связи, блокираторы связи, а также конфиденциальная связь. Все это возможно 
интегрировать для обеспечения возможности оперативного вызова службы безопасности или 
проведения конфиденциальных переговоров.
            </div>
            <div class="scheme-content-item camera"  style="display: none;">
Видеонаблюдение за обстановкой: 
Мы готовы предложить, как стандартные решения, так и индивидуальные - радиационно-стойкая
оптика, тепловизоры, датчики проникновения и движения.
Вы получите достоверную информацию о ситуации вне защитного сооружения. Информацию о 
температуре, радиационном фоне, степени загрязненности воздуха опасными отравляющими 
веществами.
            </div>
            <div class="scheme-content-item biometry"  style="display: none;">
Система доступа в защитное сооружение: 
Возможна интеграция биометрических систем доступа - таких как система доступа по отпечаткам 
пальцев или сетчатке глаз
            </div>
            <div class="scheme-content-item disguise"  style="display: none;">
Маскировка: 
Мы можем замаскировать вход в защитное сооружение под шкаф или другие элементы 
интерьера. А также замаскировать коммуникации воздуховодов под объекты ландшафта.
            </div>
            <div class="scheme-content-item bomb"  style="display: none;">
Противовзрывные клапаны: 
Защита вашего бункера от ударной волны. Важность данных клапанов обусловлена тем, что они 
не только глушат ударную волну, но и тем самым обеспечивают работоспособность 
фильтровентиляционных систем, которые отвечают за обеспечение чистым и пригодным для 
дыхания воздухом.
            </div>
            <div class="scheme-content-item ventilation"  style="display: none;">
Фильтровентиляционная установка, регенеративная: 
Системы, обеспечивающие комфортное дыхание внутри защитного сооружения. Представляют 
собой систему, забирающую воздух снаружи защитного сооружения, фильтрует воздух от 
радиационных частиц, всех известных боевых отравляющих газов, биологического оружия. 
Создается подпор чистым воздухом внутри защитного сооружения. Избыточное давление 
сбрасывается через клапаны избыточного давления. В результате чего происходит обновление 
воздуха внутри защитного сооружения, исключая образование углекислоты, влияющей на 
самочувствие укрываемых. При потере питания установки оборудованы ручными приводами 
вентиляторов. При возникновении необходимости полной изоляции помещения, и отсутствии 
возможности забора воздуха извне по разным причинам, в проект могут быть включены 
регенеративные агрегаты. Работают по принципу появления химической реакции регенерации 
воздуха из кислорода и двуокиси углерода.
            </div>
            <div class="scheme-content-item laser"  style="display: none;">
Защитногерметичные и пуленепробиваемые двери: 
Такие двери обеспечивают защиту сооружения от ударной волны, а также от проникновения в 
защитное сооружение нежелательных лиц. Такие двери практически невозможно взломать или 
вскрыть.
